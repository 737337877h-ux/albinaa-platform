import { ApiError } from './api';

/**
 * رسائل خطأ عربية موحّدة لكل الواجهة — لا تُعرض رسائل النظام الخام أبدًا.
 * مستخدمة في صفحة الدخول وفي كل استعلامات البيانات (Dashboard وغيرها).
 */
export function friendlyApiError(err: unknown): string {
  if (err instanceof ApiError) {
    if (err.status === 401) return 'انتهت صلاحية الجلسة. سجّل الدخول من جديد.';
    if (err.status === 403) return 'لا تملك صلاحية الوصول إلى هذا القسم.';
    if (err.status === 404) return 'العنصر المطلوب غير موجود.';
    if (err.status === 429) return 'تم تجاوز عدد المحاولات المسموح بها. حاول بعد قليل.';
    if (err.status === 0) return 'تعذّر الاتصال بالخادم. تحقق من اتصالك بالشبكة.';
    if (err.status >= 500) return 'الخادم يواجه مشكلة مؤقتة. حاول مرة أخرى بعد قليل.';
    return 'حدث خطأ غير متوقع. حاول مرة أخرى.';
  }
  return 'حدث خطأ غير متوقع. حاول مرة أخرى.';
}

/**
 * تاريخ اليوم بصيغة YYYY-MM-DD **بتوقيت المنشأة** (اليمن، UTC+3 دائمًا —
 * بلا توقيت صيفي)، لا بتوقيت UTC الخام للمتصفح.
 *
 * تصحيح مراجعة Dashboard: `new Date().toISOString().slice(0,10)` يعطي تاريخ
 * UTC، الذي قد يسبق تاريخ اليوم المحلي في اليمن بيوم كامل قرب منتصف الليل
 * (مثلاً 23:30 بتوقيت اليمن = 20:30 UTC من اليوم نفسه فعلاً، لكن الحالات
 * القريبة من الحدود تختلف حسب توقيت جهاز العميل). نزيح الوقت +3 ساعات قبل
 * أخذ التاريخ لضمان مطابقة يوم العمل الفعلي في اليمن دائمًا، بصرف النظر عن
 * توقيت جهاز المستخدم. القيمة الناتجة تُرسَل كما هي إلى الـAPI، الذي يفسّرها
 * بدوره كحدود يوم بتوقيت المنشأة (راجع backend/src/common/org-time.ts).
 */
const ORG_UTC_OFFSET_MINUTES = 3 * 60;

export function todayISO(): string {
  const shifted = new Date(Date.now() + ORG_UTC_OFFSET_MINUTES * 60_000);
  return shifted.toISOString().slice(0, 10);
}
